public class Calculator {

     int add(int a , int b ){
         return a + b ;
     }

     int add (int a , int b , int c ){ // dodawanie

         return  a + b + c ;

     }

     int substract (int a , int b ){ //odejmowanie

         return a - b ;
     }

     int substract ( int a , int b , int c ){
         return ( a - b ) - c ;
     }
}
